const BAD_WORDS = ['씨발','시발','쉬발','씌발','씨바','시바','씨불','시불','씨빨','시빨','씨뱔','시뱔',
    '씨8','시8','ㅆㅂ','ㅅㅂ','십발','씹발','씹할','씹할놈',
    '병신','븅신','병맛','병크','병딱',
    '개새끼','개새','개같','개쉐','개샛키','개샹','개썅','개지랄',
    '좆','좇','졷','좆같','좆밥','좆망',
    '존나','존내','존니','죤나','존맛같','존맵','존웃',
    '미친','미친놈','미친년','미쳤네','돌아이','또라이','똘아이',
    '지랄','지럴','지롤','지랄하네','지랄맞네',
    '염병','염병하네','엠병','엠창','염창',
    '꺼져','꺼지라','꺼지세요',
    '뒤져','뒤져라','뒤지라','디져','디져라','죽어라',
    '썅','쌍놈','쌍년','쌍것','썅것',
    '섹스','빠구리','빠굴','보지','자지'];
const MASK_CHAR = '•';

export function maskProfanity(input: string) {
    let out = input;
    for (const w of BAD_WORDS) {
        const re = new RegExp(w.replace(/[.*+?^${}()|[\]\\]/g, '\\$&'), 'gi');
        out = out.replace(re, (m) => MASK_CHAR.repeat(m.length));
    }
    return out;
}
